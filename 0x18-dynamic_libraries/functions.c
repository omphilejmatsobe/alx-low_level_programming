/**
 * add - sum of a and b
 * @a: operand 1
 * @b: operand 2
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substract of a and b
 * @a: operand 1
 * @b: operand 2
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply of a and b
 * @a: operand 1
 * @b: operand 2
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide of a and b
 * @a: operand 1
 * @b: operand 2
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - module of a and b
 * @a: operand 1
 * @b: operand 2
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
